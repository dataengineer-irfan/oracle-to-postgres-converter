    def _call_faker(self, tag: str, max_length: int) -> str:
        fake = self._fake
        if tag == "license":
            return f"ND{random.randint(100000, 9999999)}"
        if tag == "npi":
            return str(random.choice([1, 2])) + "".join(
                str(random.randint(0, 9)) for _ in range(9)
            )
        if tag == "dea":
            first  = random.choice(["A", "B", "F", "G", "M", "P"])
            second = random.choice("ABCDEFGHIJKLMNOPQRSTUVWXYZ")
            digits = [random.randint(0, 9) for _ in range(6)]
            total  = (digits[0] + digits[2] + digits[4]) + 2 * (digits[1] + digits[3] + digits[5])
            digits.append(total % 10)
            return f"{first}{second}{''.join(map(str, digits))}"
        if tag == "tin":
            return "".join(str(random.randint(0, 9)) for _ in range(9))
        if tag == "uuid":
            return str(uuid.uuid4())
        if tag == "provider_id":
            return fake.bothify("??#######").upper()

        dispatch: dict[str, Any] = {
            "last_name"     : fake.last_name,
            "first_name"    : fake.first_name,
            "name"          : fake.name,
            "street_address": fake.street_address,
            "city"          : fake.city,
            "state_abbr"    : fake.state_abbr,
            "zipcode"       : fake.zipcode,
            "phone_number"  : fake.phone_number,
            "email"         : fake.email,
            "url"           : fake.url,
            "company"       : fake.company,
            "date_of_birth" : lambda: fake.date_of_birth().strftime("%Y-%m-%d"),
        }
        fn = dispatch.get(tag)
        if fn is None:
            return fake.word()
        return str(fn())
