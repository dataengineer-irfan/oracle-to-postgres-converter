def _strip_prefix(name: str) -> str:
    """Remove leading p_ / g_ / t_ / l_ prefixes."""
    for pre in ("p_", "g_", "t_", "l_"):
        if name.startswith(pre):
            return name[len(pre):]
    return name


def _fk_suffix_matches(child_col: str, parent_col: str) -> bool:
    """
    Return True if child_col looks like a FK to parent_col.

    Rules (highest to lowest priority):
      1. Exact name match               p_sys_id == p_sys_id
      2. Child ends with _<parent_col>  p_grp_sys_id ends with _sys_id
      3. Child ends with _<parent_core> after prefix strip
    """
    if child_col == parent_col:
        return True
    c_core = _strip_prefix(child_col)
    p_core = _strip_prefix(parent_col)
    if c_core.endswith("_" + parent_col):
        return True
    if c_core.endswith("_" + p_core):
        return True
    if c_core == p_core:
        return True
    return False


