"""
data_generator.py — Synthesize realistic test rows from learned ColumnProfiles.

Architecture
────────────
1. PatternAnalyzer provides per-column generation strategies.
2. DataGenerator uses an EXPLICIT FK registry:
   - PROVIDER_FK_MAP contains authoritative FK->PK declarations for every known
     relationship in the Provider schema.  This is merged with heuristic detection
     at startup; explicit entries always take priority.
   - Tracks every generated PK value under its column name.
   - Child FK columns look up their parent pool and pick an existing value.
3. Tables are generated in topological order (parents first, always).
4. If the topological sort cannot satisfy a dependency (e.g. circular), a
   post-generation _fix_orphan_fks() pass repairs any remaining orphans.
5. Zero orphans are guaranteed after the repair pass.

FK Integrity Design
────────────────────
- _pk_registry  : { table_name: { col_name: [generated values] } }
  Stores every PK/unique value generated by a parent table.

- _fk_map : { (child_table, child_col): (parent_table, parent_col) }
  Explicit mapping built once at startup from PROVIDER_FK_MAP + heuristics.

- generate_all() : multi-round loop — defers tables whose parent pools are not
  yet populated until later rounds.

- _fix_orphan_fks() : final safety net — repairs any FK value that is not found
  in the parent pool by replacing it with a random valid parent PK.

- validate_fk_integrity() : post-generation audit returning {(child,col): orphan_count}.
"""
