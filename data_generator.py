    def _random_date(self, prof: ColumnProfile) -> date:
        d_min = _parse_date_bound(prof.date_min) or date(2000, 1, 1)
        d_max = _parse_date_bound(prof.date_max) or date(2030, 12, 31)
        if d_min > d_max:
            d_min, d_max = d_max, d_min
        delta = (d_max - d_min).days
        return d_min + timedelta(days=random.randint(0, max(delta, 0)))

    def _random_timestamp(self, prof: ColumnProfile) -> datetime:
        d_min = _parse_date_bound(prof.date_min) or date(2000, 1, 1)
        d_max = _parse_date_bound(prof.date_max) or date(2030, 12, 31)
        if d_min > d_max:
            d_min, d_max = d_max, d_min
        delta_days = (d_max - d_min).days
        rand_d = d_min + timedelta(days=random.randint(0, max(delta_days, 0)))
        return datetime(
            rand_d.year, rand_d.month, rand_d.day,
            random.randint(0, 23),
            random.randint(0, 59),
            random.randint(0, 59),
            random.randint(0, 999999),
        )

    # ------------------------------------------------------------------ #
    # Row constraints                                                      #
    # ------------------------------------------------------------------ #

    def _apply_row_constraints(
        self,
        row     : dict[str, Any],
        profiles: dict[str, ColumnProfile],
    ) -> None:
        from config import AUDIT_USER

        for col in row:
            if col in ("g_aud_user_id", "g_aud_add_user_id",
                       "g_aud_upd_user_id", "g_web_user_id"):
                row[col] = AUDIT_USER

        now_dt  = datetime.now()
        add_col = next((c for c in row if c == "g_aud_add_ts"), None)
        aud_col = next((c for c in row if c == "g_aud_ts"), None)

        if add_col and row[add_col] is not None:
            row[add_col] = now_dt - timedelta(
                days=random.randint(1, 365),
                seconds=random.randint(0, 86400),
            )
        if aud_col and row[aud_col] is not None:
            base = row[add_col] if (add_col and row[add_col] is not None) else now_dt
            row[aud_col] = base + timedelta(
                days=random.randint(0, 30),
                seconds=random.randint(0, 86400),
            )

        begin_patterns = ["beg_dt", "start_dt", "eff_dt", "add_dt"]
        end_patterns   = ["end_dt", "term_dt", "cancel_dt", "rever_dt"]

        begins = [c for c in row if isinstance(row[c], (date, datetime))
                  and any(p in c for p in begin_patterns)]
        ends   = [c for c in row if isinstance(row[c], (date, datetime))
                  and any(p in c for p in end_patterns)]

        for b_col in begins:
            for e_col in ends:
                b_val = row[b_col]
                e_val = row[e_col]
                if b_val and e_val:
                    b_d = b_val.date() if isinstance(b_val, datetime) else b_val
                    e_d = e_val.date() if isinstance(e_val, datetime) else e_val
                    if e_d <= b_d:
                        new_e = b_d + timedelta(days=random.randint(30, 365 * 5))
                        row[e_col] = (
                            datetime.combine(new_e, e_val.time())
                            if isinstance(e_val, datetime)
                            else new_e
                        )

    def _format_date_val(self, val: date | datetime, prof: ColumnProfile) -> str:
        if isinstance(val, datetime):
            if prof.is_oracle_fmt:
                mon = _MONTH_FROM_NUM[val.month]
                h12 = val.hour % 12 or 12
                mer = "AM" if val.hour < 12 else "PM"
                ns  = val.microsecond * 1000
                return (
                    f"{val.day:02d}-{mon}-{str(val.year)[-2:]:>02s} "
                    f"{h12:02d}.{val.minute:02d}.{val.second:02d}.{ns:09d} {mer}"
                )
            return val.strftime("%Y-%m-%d %H:%M:%S.%f")
        else:
            if prof.is_oracle_fmt:
                mon = _MONTH_FROM_NUM[val.month]
                return f"{val.day:02d}-{mon}-{str(val.year)[-2:]:>02s}"
            return val.isoformat()

